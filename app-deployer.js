/**
 * app-deployer.js — Automated App Scaffolding & Deployment for Solomon Bot
 * Generates Next.js + Tailwind boilerplate, initializes git, and pushes to GitHub.
 */
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const APPS_DIR = '/root/solomon-apps';

/**
 * Scaffold a new Next.js + Tailwind app
 * @param {string} appName - Name of the app (used as directory name)
 * @param {string} description - Brief description of what the app does
 * @returns {object} Result with path and file list
 */
function scaffoldNextApp(appName, description = '') {
  try {
    const safeName = appName.toLowerCase().replace(/[^a-z0-9-]/g, '-').slice(0, 40);
    const appPath = path.join(APPS_DIR, safeName);
    
    // Create directory structure
    if (!fs.existsSync(APPS_DIR)) fs.mkdirSync(APPS_DIR, { recursive: true });
    if (fs.existsSync(appPath)) {
      return { error: `App directory already exists: ${appPath}` };
    }
    
    const dirs = [
      appPath,
      path.join(appPath, 'src'),
      path.join(appPath, 'src', 'app'),
      path.join(appPath, 'src', 'components'),
      path.join(appPath, 'public')
    ];
    dirs.forEach(d => fs.mkdirSync(d, { recursive: true }));
    
    // package.json
    const packageJson = {
      name: safeName,
      version: '0.1.0',
      private: true,
      description: description || `${appName} — generated by Solomon`,
      scripts: {
        dev: 'next dev',
        build: 'next build',
        start: 'next start',
        lint: 'next lint'
      },
      dependencies: {
        next: '^14.0.0',
        react: '^18.2.0',
        'react-dom': '^18.2.0'
      },
      devDependencies: {
        tailwindcss: '^3.4.0',
        postcss: '^8.4.0',
        autoprefixer: '^10.4.0',
        typescript: '^5.0.0',
        '@types/react': '^18.2.0',
        '@types/node': '^20.0.0'
      }
    };
    fs.writeFileSync(path.join(appPath, 'package.json'), JSON.stringify(packageJson, null, 2));
    
    // tailwind.config.js
    fs.writeFileSync(path.join(appPath, 'tailwind.config.js'), `/** @type {import('tailwindcss').Config} */
module.exports = {
  content: ['./src/**/*.{js,ts,jsx,tsx,mdx}'],
  theme: { extend: {} },
  plugins: []
};
`);
    
    // postcss.config.js
    fs.writeFileSync(path.join(appPath, 'postcss.config.js'), `module.exports = {
  plugins: {
    tailwindcss: {},
    autoprefixer: {}
  }
};
`);
    
    // next.config.js
    fs.writeFileSync(path.join(appPath, 'next.config.js'), `/** @type {import('next').NextConfig} */
const nextConfig = {};
module.exports = nextConfig;
`);
    
    // tsconfig.json
    fs.writeFileSync(path.join(appPath, 'tsconfig.json'), JSON.stringify({
      compilerOptions: {
        target: 'es5',
        lib: ['dom', 'dom.iterable', 'esnext'],
        allowJs: true,
        skipLibCheck: true,
        strict: true,
        noEmit: true,
        esModuleInterop: true,
        module: 'esnext',
        moduleResolution: 'bundler',
        resolveJsonModule: true,
        isolatedModules: true,
        jsx: 'preserve',
        incremental: true,
        paths: { '@/*': ['./src/*'] }
      },
      include: ['next-env.d.ts', '**/*.ts', '**/*.tsx'],
      exclude: ['node_modules']
    }, null, 2));
    
    // globals.css
    fs.writeFileSync(path.join(appPath, 'src', 'app', 'globals.css'), `@tailwind base;
@tailwind components;
@tailwind utilities;
`);
    
    // layout.tsx
    fs.writeFileSync(path.join(appPath, 'src', 'app', 'layout.tsx'), `import './globals.css';
import type { Metadata } from 'next';

export const metadata: Metadata = {
  title: '${appName}',
  description: '${description || appName}'
};

export default function RootLayout({ children }: { children: React.ReactNode }) {
  return (
    <html lang="en">
      <body className="min-h-screen bg-gray-50">{children}</body>
    </html>
  );
}
`);
    
    // page.tsx
    fs.writeFileSync(path.join(appPath, 'src', 'app', 'page.tsx'), `export default function Home() {
  return (
    <main className="flex min-h-screen flex-col items-center justify-center p-8">
      <h1 className="text-4xl font-bold text-gray-900 mb-4">${appName}</h1>
      <p className="text-lg text-gray-600">${description || 'Built with Solomon AI'}</p>
    </main>
  );
}
`);
    
    // .gitignore
    fs.writeFileSync(path.join(appPath, '.gitignore'), `node_modules/
.next/
out/
.env
.env.local
`);
    
    // README.md
    fs.writeFileSync(path.join(appPath, 'README.md'), `# ${appName}

${description || 'Generated by Solomon AI'}

## Getting Started

\`\`\`bash
npm install
npm run dev
\`\`\`

Open [http://localhost:3000](http://localhost:3000) to view the app.
`);
    
    return {
      path: appPath,
      name: safeName,
      files: fs.readdirSync(appPath, { recursive: true }).length + ' files created',
      nextStep: 'Run: cd ' + appPath + ' && npm install && npm run dev'
    };
  } catch (err) {
    return { error: `Scaffold error: ${err.message}` };
  }
}

/**
 * Initialize a git repository in the given path
 * @param {string} repoPath - Path to initialize git in
 * @returns {object} Result
 */
function initGitRepo(repoPath) {
  try {
    if (!fs.existsSync(repoPath)) {
      return { error: `Path does not exist: ${repoPath}` };
    }
    execSync('git init', { cwd: repoPath, stdio: 'pipe' });
    execSync('git add -A', { cwd: repoPath, stdio: 'pipe' });
    execSync('git commit -m "Initial commit — scaffolded by Solomon AI"', { cwd: repoPath, stdio: 'pipe' });
    return { success: true, path: repoPath, message: 'Git repo initialized with initial commit' };
  } catch (err) {
    return { error: `Git init error: ${err.message}` };
  }
}

/**
 * Push a local repo to GitHub (requires gh CLI or git remote configured)
 * @param {string} repoPath - Path to the local repo
 * @param {string} repoName - GitHub repository name
 * @returns {object} Result with repo URL
 */
function pushToGitHub(repoPath, repoName) {
  try {
    if (!fs.existsSync(repoPath)) {
      return { error: `Path does not exist: ${repoPath}` };
    }
    
    // Try using GitHub CLI first
    try {
      const result = execSync(
        `gh repo create ${repoName} --private --source=. --remote=origin --push`,
        { cwd: repoPath, stdio: 'pipe', timeout: 30000 }
      ).toString().trim();
      return { success: true, url: `https://github.com/${repoName}`, output: result };
    } catch (ghErr) {
      // Fallback: try git remote add + push
      const ghToken = process.env.GITHUB_TOKEN || '';
      if (!ghToken) {
        return { error: 'No GITHUB_TOKEN or gh CLI available. Set GITHUB_TOKEN env var or install gh CLI.' };
      }
      execSync(`git remote add origin https://${ghToken}@github.com/${repoName}.git`, { cwd: repoPath, stdio: 'pipe' });
      execSync('git push -u origin main', { cwd: repoPath, stdio: 'pipe', timeout: 30000 });
      return { success: true, url: `https://github.com/${repoName}` };
    }
  } catch (err) {
    return { error: `GitHub push error: ${err.message}` };
  }
}

module.exports = {
  scaffoldNextApp,
  initGitRepo,
  pushToGitHub
};
